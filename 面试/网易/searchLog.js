function search_log(logs, tss) {
  let len1 = logs.length, len2 = tss.length
  let res = new Array(len2).fill(-1)
  for (let i = 0; i < len2; i++) {
    let ts = tss[i]
    console.log('ts', i, ts)
    for (let j = 0; j < len1; j++) {
      let log = logs[j]
      console.log('log', j, log)
      if (ts >= log[0] && ts <= log[1]) {
        if(res[i] === -1){
          res[i] = [j]
          console.log('empty', res)
        }
        else {
          res[i].push(j)
          console.log('not empty', res)
        }
      }
    }
    // 没找到
    if(res[i] === -1){
      res[i] = []
      console.log('not found', res)
    }
  }
  return res;
}

